  const newComment = {
    body: req.body.body,
    createdAt: new Date().toISOString(),
    screamID: req.params.screamId,
    userHandle: req.user.handle,
    userImage: req.user.imageUrl,
  };

  db.doc(`/screams/${req.params.screamId}`)
    .get()
